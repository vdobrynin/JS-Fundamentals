// --> Relational or comparison operators

// > --> more than
// < --> less than
// >= --> more than equal
// <= --> less than equal

var result1 = 10 > 5
console.log(result1)

var result2 = 4 > 5
console.log(result2)

var result3 = 4 < 5
console.log(result3)
 
var result4 = 5 < 5
console.log(result4)

var result5 = 5 > 5
console.log(result5) 

var result6 = 5 >= 5
console.log(result6) 

var result7 = 6 >= 5 
console.log(result7)

var result8 = 4 >= 5
console.log(result8)

var result9 = 6 <= 5
console.log(result9)

